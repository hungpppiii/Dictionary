    public void setVisiblePanel(JPanel panel1, JPanel panel2, JPanel panel3, JPanel panel4, JPanel panel5) {
        View.setVisible(true);
        panel1.setVisible(true);
        panel2.setVisible(false);
        panel3.setVisible(false);
        panel4.setVisible(false);
        panel5.setVisible(false);
        Welcome.setVisible(false);
    }
    public void setColorButtonMouseEntered(boolean clickedButton, JPanel Button) {
        if (clickedButton == false) {
            setColorMouseEnter(Button);
        }   
    }
    public void setColorButtonMouseExited(boolean clickedButton, JPanel Button) {
        if (clickedButton == true) {
            setColorMousePressed(Button);
        } else {
            resetColor(Button);
        }
    }
    public void setColorMousePressed(JPanel panel1, JPanel panel2, JPanel panel3, JPanel panel4, JPanel panel5) {
        setColorMousePressed(panel1);
        resetColor(panel2);
        resetColor(panel3);
        resetColor(panel4);
        resetColor(panel5);
    }
    public void setColorMouseEnter(JPanel panel) {
        panel.setBackground(new Color(93,100,118));
    }
    public void setColorMousePressed(JPanel panel) {
        panel.setBackground(new Color(39,55,80));
    }
    
    public void resetColor(JPanel panel) {
        panel.setBackground(new Color(23,33,53));
    }
    public void pressesChangeButton() {
        if ("Vietnamese".equals(nameLanguage1.getText())) {
            nameLanguage1.setText("English");
            nameLanguage2.setText("Vietnamese");
            iconLanguage1.setIcon(iconEngland);
            iconLanguage2.setIcon(iconVietnam);
        } else if ("English".equals(nameLanguage1.getText())) {
            nameLanguage1.setText("Vietnamese");
            nameLanguage2.setText("English");
            iconLanguage1.setIcon(iconVietnam);
            iconLanguage2.setIcon(iconEngland);
        }
    }
    public void notClickedButton() {
        clickedSearchButton = false;
        clickedAddButton = false;
        clickedComingSoonButton = false;
        clickedDeleteButton = false;   
        clickedRecoverButton = false;   
    }          
