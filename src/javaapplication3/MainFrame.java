    private void textPanelKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textPanelKeyReleased
        // TODO add your handling code here:
        if (KeyEvent.VK_ENTER== evt.getKeyCode()) {
            String word = textPanel.getText();
            if ("English".equals(nameLanguage1.getText())) {
                if (!"".equals(textPanel.getText())) {
                    int index;
                    index = dictManagement.dictionaryLookup(word);
                    if (index < 0) {
                        meanOfWord.setText("This word is unavailable");
                        meanOfWord.setForeground(Color.red);
                    } else {
                        meanOfWord.setText(Dictionary.words.get(index).getWordExplain());
                        meanOfWord.setForeground(Color.black);
                        //tyoeOfWord.setText(Dictionary.words.get(index).getWordType());           
                    }
                    wordNeedTranslating.setText(textPanel.getText());
                    contentWelcome.setVisible(false);
                    contentTranslate.setVisible(true);
                }
            } else {
                if (!"".equals(textPanel.getText())) {
                    try {
                        meanOfWord.setText(tran.translate("vi", "en", word));
                    } catch (IOException ex) {
                        Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    meanOfWord.setForeground(Color.black);
                        //tyoeOfWord.setText(Dictionary.words.get(index).getWordType());           
                    wordNeedTranslating.setText(textPanel.getText());
                    contentWelcome.setVisible(false);
                    contentTranslate.setVisible(true);
                }
            }
            translateButton_ViewChoose_Search.setBackground(new Color(122,154,232));
        }
    }//GEN-LAST:event_textPanelKeyReleased

